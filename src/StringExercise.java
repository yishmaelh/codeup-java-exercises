public class StringExercise {

    public static void main( String[] args){

        String firstString = "We don't need no education" + "\n" + "We don't need no thought control";;
        System.out.println(firstString);

        String secondString = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(secondString);

        String thirdString = "In windows, the main drive is usually C:\\";
        System.out.println(thirdString);

        String fourthString = " I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(fourthString);
    }
}
